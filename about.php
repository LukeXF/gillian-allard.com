Gillian Allard (b. 1966) is a British photographer, born and based in Suffolk, UK.  She began her career as a photojournalist and her work appeared in many publications including The Sunday Times, The Guardian and The Observer Magazine. 
<br>
<br>Her landscape series and subsequent work with photographic imagery on wood and studies of decaying forms, emerged from a desire to escape the constraints of the human subject and explore the emotional world through an interaction with nature. She is a MA graduate of the Royal College of Art.
